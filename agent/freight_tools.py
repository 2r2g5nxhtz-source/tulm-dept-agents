"""
Freight workflow tools для @tulm_freight_bot.
Адаптация прототипа CrewAI (см. ~/agents/tulm/experiments/) на наш LangGraph стек.
Используются ReAct агентом, тот же паттерн что у dept-ботов.

ВАЖНО (2026-05-03): тарифы и точные маршруты — заглушки удалены.
Бот ВСЕГДА переадресует запросы стоимости и точного маршрута диспетчеру.
Реальные данные нужно получить от Бяшимова М. (ГД) и зашить в _RATES/_ROUTES,
ИЛИ подключить к БД tariffs (предпочтительнее).
"""
from langchain_core.tools import tool


# Маршруты ТЛЦТ — РЕАЛЬНЫХ данных пока НЕТ.
# Старые "ТМТМ" заглушки удалены — ТЛЦТ их не использует.
# Когда ГД даст актуальные коридоры (CASCA+, etc.) — добавить сюда.
_ROUTES: dict = {}

# Документы по странам назначения — оставляем, требования таможни не меняются часто
_BASE_DOCS = ["Инвойс", "Упаковочный лист", "CMR/коносамент", "Сертификат происхождения"]
_COUNTRY_DOCS = {
    "азербайджан": ["Разрешение ГТК АЗ"],
    "казахстан":   ["Транзитная декларация ЕАЭС"],
    "узбекистан":  ["Транзитная декларация ЕАЭС"],
    "россия":      ["Транзитная декларация ЕАЭС"],
    "турция":      ["EUR.1 или A.TR сертификат"],
    "грузия":      ["DCFTA сертификат"],
    "иран":        ["Лицензия МИД ТМ", "Phytosanitary certificate"],
    "китай":       ["GB-стандарты соответствия", "GACC регистрация"],
    "афганистан":  ["Лицензия МИД ТМ"],
}


@tool
def check_route_feasibility(origin: str, destination: str, mode: str) -> str:
    """Проверить доступность маршрута ТЛЦТ.
    origin/destination — города, mode — sea/rail/multimodal.
    Возвращает: либо найденный маршрут из справочника, либо переадресацию диспетчеру."""
    key = (origin.strip().lower(), destination.strip().lower(), mode.strip().lower())
    found = _ROUTES.get(key)
    if found:
        return found
    return (
        f"📍 Маршрут {origin} → {destination} ({mode}): "
        f"требует подтверждения диспетчера ТЛЦТ. "
        f"Точные коридоры, промежуточные точки и сроки — на согласовании с операционным отделом. "
        f"НЕ называй клиенту конкретный маршрут или транзитное время — это сделает диспетчер."
    )


@tool
def estimate_cost(cargo_type: str, weight_ton: float, mode: str) -> str:
    """Предварительная оценка стоимости — НЕ ВОЗВРАЩАЕТ конкретные цифры.
    Стоимость ТЛЦТ зависит от десятков параметров (объём партии, сезон, валюта,
    подвижной состав, тип груза и т.д.) и считается диспетчером по официальным тарифам.
    Бот НЕ должен называть клиенту цены."""
    return (
        f"💰 Стоимость перевозки {weight_ton} тонн ({cargo_type}, {mode}) — "
        f"рассчитывается диспетчером ТЛЦТ индивидуально по официальным тарифам. "
        f"НЕ называй клиенту конкретную сумму. Скажи: «стоимость уточнит диспетчер в течение 24 часов»."
    )


@tool
def check_required_docs(cargo_type: str, destination_country: str) -> str:
    """Список обязательных документов для таможенного оформления.
    cargo_type: general / bulk / container / hazmat
    destination_country: страна назначения (азербайджан, турция, иран и т.д.)"""
    docs = list(_BASE_DOCS)
    extra = _COUNTRY_DOCS.get(destination_country.strip().lower(), [])
    docs.extend(extra)
    if cargo_type.strip().lower() == "hazmat":
        docs.extend(["MSDS (паспорт безопасности)", "Разрешение на перевозку ОГ (опасный груз)"])
    return "📋 **Документы:** " + ", ".join(docs)
